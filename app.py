@app.route("/api/hardware/data", methods=["POST"])
def receive_hardware_data():
    """
    Endpoint to receive live hardware data from test.py
    Expected JSON: {
        "voltage": float,
        "current": float,
        "frequency": float,
        "fault": string
    }
    """
    global hardware_data, hardware_history
    
    try:
        data = request.get_json()
        
        # Validate data
        if not all(key in data for key in ["voltage", "current", "frequency", "fault"]):
            return jsonify({"error": "Missing required fields"}), 400
        
        # Update hardware data with thread safety
        with data_lock:
            hardware_data["voltage"] = float(data["voltage"])
            hardware_data["current"] = float(data["current"])
            hardware_data["frequency"] = float(data["frequency"])
            hardware_data["fault"] = str(data["fault"])
            hardware_data["timestamp"] = datetime.now().isoformat()
            hardware_data["status"] = "CONNECTED"
            
            # Store in history
            hardware_history.append({
                "voltage": hardware_data["voltage"],
                "current": hardware_data["current"],
                "frequency": hardware_data["frequency"],
                "fault": hardware_data["fault"],
                "timestamp": hardware_data["timestamp"]
            })
        
        return jsonify({"status": "SUCCESS", "message": "Data received"}), 200
        
    except Exception as e:
        print(f"Error receiving hardware data: {e}")
        return jsonify({"status": "ERROR", "message": str(e)}), 500


