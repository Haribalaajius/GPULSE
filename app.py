@app.route("/api/analytics")
@login_required
def get_analytics():
    """Returns analytics data for the analytics page"""
    try:
        with data_lock:
            if not hardware_history:
                return jsonify({
                    "stats": {
                        "voltage": {"min": 0, "max": 0, "avg": 0},
                        "current": {"min": 0, "max": 0, "avg": 0},
                        "frequency": {"min": 0, "max": 0, "avg": 0},
                        "total_readings": 0
                    },
                    "voltage_distribution": [],
                    "current_distribution": [],
                    "fault_counts": [],
                    "fault_categories": [],
                    "hourly_data": [{"name": "Voltage", "data": []}, {"name": "Current", "data": []}]
                })
            
            # Calculate statistics
            voltages = [r["voltage"] for r in hardware_history]
            currents = [r["current"] for r in hardware_history]
            frequencies = [r["frequency"] for r in hardware_history]
            faults = [r["fault"] for r in hardware_history]

            # Count fault types
            fault_counts = {}
            for fault in faults:
                fault_counts[fault] = fault_counts.get(fault, 0) + 1

            stats = {
                "voltage": {
                    "min": min(voltages) if voltages else 0,
                    "max": max(voltages) if voltages else 0,
                    "avg": sum(voltages) / len(voltages) if voltages else 0
                },
                "current": {
                    "min": min(currents) if currents else 0,
                    "max": max(currents) if currents else 0,
                    "avg": sum(currents) / len(currents) if currents else 0
                },
                "frequency": {
                    "min": min(frequencies) if frequencies else 0,
                    "max": max(frequencies) if frequencies else 0,
                    "avg": sum(frequencies) / len(frequencies) if frequencies else 0
                },
                "total_readings": len(hardware_history)
            }

            return jsonify({
                "stats": stats,
                "voltage_distribution": voltages[-50:] if len(voltages) > 50 else voltages,
                "current_distribution": currents[-50:] if len(currents) > 50 else currents,
                "fault_counts": list(fault_counts.values()),
                "fault_categories": list(fault_counts.keys()),
                "hourly_data": [
                    {"name": "Voltage", "data": voltages[-24:] if len(voltages) > 24 else voltages},
                    {"name": "Current", "data": currents[-24:] if len(currents) > 24 else currents}
                ]
            })
    except Exception as e:
        return jsonify({"error": str(e)}), 500

