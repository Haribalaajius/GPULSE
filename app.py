@app.route("/api/alerts")
@login_required
def get_alerts():
    """Returns all alerts"""
    try:
        with data_lock:
            # Generate alerts from recent faults
            recent_alerts = []
            fault_types = {
                'SHORT_CIRCUIT_FAULT': {'type': 'critical', 'icon': 'fa-exclamation-circle', 'title': 'Short Circuit'},
                'HIGH_VOLTAGE_FAULT': {'type': 'critical', 'icon': 'fa-lightning-bolt', 'title': 'High Voltage'},
                'OPEN_VOLTAGE_FAULT': {'type': 'warning', 'icon': 'fa-exclamation-triangle', 'title': 'Open Circuit'},
                'FREQUENCY_FAULT': {'type': 'warning', 'icon': 'fa-wave-square', 'title': 'Frequency Unstable'},
                'NORMAL': {'type': 'info', 'icon': 'fa-check-circle', 'title': 'System Normal'}
            }
            
            for reading in list(hardware_history)[-50:]:
                fault = reading['fault']
                if fault in fault_types:
                    alert_info = fault_types[fault]
                    recent_alerts.append({
                        'type': alert_info['type'],
                        'icon': alert_info['icon'],
                        'title': alert_info['title'],
                        'description': f"Fault detected: {fault}",
                        'timestamp': reading['timestamp'],
                        'resolved': fault == 'NORMAL'
                    })
            
            return jsonify({"alerts": recent_alerts})
    except Exception as e:
        return jsonify({"error": str(e)}), 500

